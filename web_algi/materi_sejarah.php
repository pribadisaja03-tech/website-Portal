    <p>
      Suku Sasak adalah etnis mayoritas di Pulau Lombok, Nusa Tenggara Barat. 
      Mereka mewakili sekitar 80% penduduk Lombok dan menjadi bagian penting dari identitas 
      sosial-budaya di wilayah tersebut. Sasak tidak hanya sekadar kelompok etnis, 
      tetapi juga sebuah peradaban yang telah membentuk tradisi, bahasa, seni, 
      dan nilai-nilai kehidupan yang diwariskan lintas generasi.
    </p>

    <h2>Asal Usul dan Sejarah Awal</h2>
    <p>
      Sejarah suku Sasak berakar dari migrasi bangsa Austronesia ribuan tahun lalu. 
      Leluhur mereka diyakini datang melalui jalur maritim, membawa bahasa, sistem kepercayaan, 
      serta keterampilan bertani dan berlayar. Pengaruh luar—terutama dari Bali, Jawa, Bugis, 
      serta pedagang Arab dan Cina—perlahan memperkaya budaya Sasak. 
      Bukti sejarah berupa situs megalitik, pura kuno, dan masjid berusia ratusan tahun 
      menunjukkan bahwa Lombok telah menjadi tempat pertemuan berbagai peradaban.
    </p>
    <p>
      Dalam catatan kerajaan-kerajaan Nusantara, Lombok kerap disebut sebagai wilayah penting. 
      Hubungan politik dan perdagangan dengan Majapahit, Bali, dan Makassar memperkuat posisi 
      Sasak dalam jaringan Nusantara. Meski kerap mengalami konflik internal dan penjajahan asing, 
      masyarakat Sasak berhasil mempertahankan jati dirinya.
    </p>

    <h2>Pengaruh Agama</h2>
    <p>
      Mayoritas masyarakat Sasak kini beragama Islam, namun proses islamisasi berlangsung panjang. 
      Islam masuk pada abad ke-16 melalui pedagang dan mubaligh dari Jawa dan Sulawesi. 
      Uniknya, ajaran Islam di Lombok berakulturasi dengan adat lokal sehingga lahirlah dua corak utama:
    </p>
    <ul>
      <li><b>Islam Wetu Telu</b> → memadukan ajaran Islam dengan tradisi Hindu-Buddha dan animisme. Mereka melaksanakan salat tiga kali sehari, bukan lima, serta masih mempertahankan ritual adat.</li>
      <li><b>Islam Waktu Lima</b> → praktik Islam sebagaimana mayoritas Muslim di dunia, yang kini lebih dominan di Lombok.</li>
    </ul>
    <p>
      Tradisi seperti <em>Bau Nyale</em> (menangkap cacing laut sebagai jelmaan Putri Mandalika) 
      dan <em>Lebaran Topat</em> (perayaan syukur seminggu setelah Idul Fitri) mencerminkan 
      betapa agama dan adat berpadu membentuk identitas Sasak.
    </p>

    <h2>Sistem Sosial dan Adat</h2>
    <p>
      Masyarakat Sasak memiliki struktur sosial yang teratur. Tradisi <em>merariq</em> 
      atau kawin lari menjadi salah satu praktik pernikahan khas. Prosesi ini melibatkan 
      "penculikan simbolis" mempelai perempuan, yang kemudian diikuti serangkaian adat, 
      termasuk mediasi keluarga dan pembayaran adat. Meski kontroversial, merariq dipandang 
      sebagai bukti kesungguhan dan tanggung jawab calon pengantin.
    </p>
    <p>
      Kehidupan sosial Sasak juga ditandai oleh tradisi gotong royong dan <em>begibung</em>, 
      yaitu makan bersama dalam satu wadah besar. Nilai kebersamaan ini menjaga solidaritas sosial 
      di tengah dinamika modernisasi.
    </p>

    <h2>Seni, Musik, dan Arsitektur</h2>
    <p>
      Budaya Sasak kaya dengan seni tradisional. <em>Gendang beleq</em>, misalnya, adalah musik 
      tabuhan gendang besar yang dimainkan berkelompok, dahulu untuk mengiringi prajurit perang, 
      kini untuk festival budaya. <em>Tari Peresean</em> menggambarkan duel antara dua pria 
      menggunakan rotan dan perisai, melambangkan keberanian dan sportivitas.
    </p>
    <p>
      Dalam seni tekstil, tenun songket Sasak adalah karya luar biasa. Setiap motif memiliki makna: 
      simbol doa, kesuburan, hingga penghormatan pada leluhur. Proses menenun tidak hanya aktivitas ekonomi, 
      tetapi juga ibadah budaya. Selain itu, rumah adat <em>Bale Tani</em> dan <em>Bale Lumbung</em> 
      mencerminkan kearifan lokal: ramah lingkungan, tahan gempa, dan sarat filosofi.
    </p>

    <h2>Bahasa Sasak</h2>
    <p>
      Bahasa Sasak termasuk rumpun Austronesia dan memiliki beberapa dialek, seperti dialek Ngeno-Ngene, 
      Meno-Mene, serta Kuto-Kute. Bahasa ini bukan hanya alat komunikasi, tetapi juga identitas budaya. 
      Sayangnya, pengaruh bahasa Indonesia yang semakin dominan membuat penggunaan bahasa Sasak menurun 
      di kalangan generasi muda.
    </p>

    <h2>Tradisi dan Festival</h2>
    <p>
      Beberapa tradisi besar masih dipertahankan hingga kini:
    </p>
    <ul>
      <li><b>Bau Nyale</b> → menangkap nyale (cacing laut) sebagai simbol pengorbanan Putri Mandalika.</li>
      <li><b>Lebaran Topat</b> → perayaan syukur masyarakat Sasak setelah Idul Fitri, ditandai dengan makan ketupat di pantai.</li>
      <li><b>Peresean</b> → duel tradisional pria sebagai simbol keberanian.</li>
    </ul>

    <h2>Tantangan Modern</h2>
    <p>
      Globalisasi dan pariwisata memberi dampak ganda pada budaya Sasak. 
      Di satu sisi, tradisi menjadi daya tarik wisata yang meningkatkan ekonomi. 
      Di sisi lain, ada risiko komersialisasi yang mereduksi nilai sakral adat. 
      Perubahan gaya hidup juga mengancam keberlanjutan bahasa, tradisi, dan kearifan lokal.
    </p>

    <h2>Kesimpulan</h2>
    <p>
      Sejarah dan budaya Sasak adalah warisan berharga yang membentuk jati diri Lombok. 
      Dari sistem sosial, seni, tradisi, hingga bahasa, semua aspek menunjukkan betapa kaya 
      dan dinamisnya peradaban Sasak. Tantangan globalisasi menuntut masyarakat untuk bijak: 
      melestarikan budaya tanpa menolak kemajuan. Dengan demikian, budaya Sasak tidak hanya 
      menjadi kenangan masa lalu, tetapi juga pondasi masa depan.
    </p>

